export const createColorCssVariables = () => {
	const colors = Object.entries(color).map(([key, value]) => {
		const shades = Object.entries(value).map(([shade, hex]) => `--aster-color-${key}-${shade}: ${hex};`)

		return shades.join('\n')
	})

	const textColors = Object.entries(textColor).map(([variant, hex]) => `--aster-color-text-${variant}: ${hex};`)

	return [...colors, ...textColors]
}
